public class InchesToFeet
{
    public static void main(String args[])
    {
        int numOfInches = 86;
        final int inchesInAFeet = 12;
        System.out.println(numOfInches + " inches becomes " + numOfInches/inchesInAFeet + " foot and " +
                numOfInches%inchesInAFeet + " inches.");
    }
}
